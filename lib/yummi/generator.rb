module Yummi

  class Generator

    class ShellScript

      def generate
        content = <<END
#!/bin/sh

# .:: Generated by Yummi ::.

usage() {
  echo "Usage: $0 COLOR TEXT [FORMAT]"
  echo "  Because this program is intented to be used inside scripts, the colorized text will NOT come with a line break"

  exit 1
}
END
        Yummi::Color::COLORS.each do |key, pattern|
          function = <<FUNCTION
function color_#{key} {
  echo \"#{"$1".colorize(key)}\"
}
FUNCTION
          content << function
        end

        end_script = <<END

[ "$#" = "0" ] && usage

format="%s"

[ ! -z "$3" ] && format="$3"

printf "$format" $(\"color_$@\")
  
END
        content << end_script

        content
      end

      def install
        puts ".: Yummi shell script installation :.".colorize(:bold_yellow)
        name = get "Program name", "color"
        location = File.expand_path(get("Install location", "~/bin"))
        file = File.join(location, name)
        File.open(file, "w") { |f| f.write generate }
        File.chmod(0755, file)
        puts "Program installed!"
      end

      private

      def get message, default
        puts(message.colorize(:blue) << ": " << " [#{default}]".colorize(:yellow))
        input = gets.chomp
        return default if input.empty?
        input
      end
    end

  end

end